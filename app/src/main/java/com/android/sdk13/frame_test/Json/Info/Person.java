package com.android.sdk13.frame_test.Json.Info;

public class Person {
    int id;
    String name;

    public Person() {
    }

    public Person(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Person{\n" +
                "\t\tid=" + id +
                "\n\t\tname=\"" + name +
                "\"\n}\n";
    }
}
